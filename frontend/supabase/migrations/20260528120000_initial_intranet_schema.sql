create policy "Profiles are readable by authenticated users"
  on public.profiles for select
  to authenticated
  using (true);

create policy "Users can update their own profile"
  on public.profiles for update
  to authenticated
  using (auth.uid() = id)
  with check (auth.uid() = id);

create policy "Areas are readable by authenticated users"
  on public.areas for select
  to authenticated
  using (true);

create policy "Avisos are readable by authenticated users"
  on public.avisos for select
  to authenticated
  using (true);

create policy "Users can read their own demandas"
  on public.demandas for select
  to authenticated
  using (auth.uid() = user_id or auth.uid() = assignee_id);

create policy "Users can create their own demandas"
  on public.demandas for insert
  to authenticated
  with check (auth.uid() = user_id);

create policy "Users can update their own demandas"
  on public.demandas for update
  to authenticated
  using (auth.uid() = user_id or auth.uid() = assignee_id)
  with check (auth.uid() = user_id or auth.uid() = assignee_id);

create policy "Users can delete their own demandas"
  on public.demandas for delete
  to authenticated
  using (auth.uid() = user_id);

create policy "Users can read related processos"
  on public.processos for select
  to authenticated
  using (user_id is null or auth.uid() = user_id);

create policy "Users can create their own processos"
  on public.processos for insert
  to authenticated
  with check (user_id is null or auth.uid() = user_id);

create policy "Users can update their own processos"
  on public.processos for update
  to authenticated
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);

create policy "Users can read their own notifications"
  on public.notifications for select
  to authenticated
  using (auth.uid() = user_id);

create policy "Users can update their own notifications"
  on public.notifications for update
  to authenticated
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);

create policy "Users can read attachments for accessible demandas"
  on public.demanda_attachments for select
  to authenticated
  using (
    exists (
      select 1
      from public.demandas d
      where d.id = demanda_attachments.demanda_id
        and (d.user_id = auth.uid() or d.assignee_id = auth.uid())
    )
  );

create policy "Users can create attachments for their own demandas"
  on public.demanda_attachments for insert
  to authenticated
  with check (
    auth.uid() = user_id
    and exists (
      select 1
      from public.demandas d
      where d.id = demanda_attachments.demanda_id
        and d.user_id = auth.uid()
    )
  );

