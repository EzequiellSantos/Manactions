create extension if not exists "pgcrypto";

create type public.demanda_status as enum ('aberto', 'em_andamento', 'concluido', 'cancelado');
create type public.notification_type as enum ('demanda', 'aviso', 'processo');

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  avatar_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.areas (
  id text primary key,
  nome text not null,
  descricao text not null,
  icon text not null,
  cor text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.demandas (
  id bigint generated by default as identity primary key,
  titulo text not null,
  descricao text,
  area_id text references public.areas(id) on delete set null,
  status public.demanda_status not null default 'aberto',
  user_id uuid not null references auth.users(id) on delete cascade,
  assignee_id uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.avisos (
  id uuid primary key default gen_random_uuid(),
  titulo text not null,
  resumo text not null,
  autor text not null,
  destaque boolean not null default false,
  published_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.processos (
  id uuid primary key default gen_random_uuid(),
  titulo text not null,
  descricao text,
  area_id text references public.areas(id) on delete set null,
  user_id uuid references auth.users(id) on delete set null,
  last_accessed_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.notifications (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  title text not null,
  description text not null,
  type public.notification_type not null,
  read boolean not null default false,
  created_at timestamptz not null default now()
);

create table public.demanda_attachments (
  id uuid primary key default gen_random_uuid(),
  demanda_id bigint not null references public.demandas(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  bucket_id text not null default 'demandas',
  storage_path text not null,
  filename text not null,
  content_type text,
  size_bytes bigint,
  created_at timestamptz not null default now()
);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger profiles_set_updated_at
  before update on public.profiles
  for each row execute function public.set_updated_at();

create trigger areas_set_updated_at
  before update on public.areas
  for each row execute function public.set_updated_at();

create trigger demandas_set_updated_at
  before update on public.demandas
  for each row execute function public.set_updated_at();

create trigger avisos_set_updated_at
  before update on public.avisos
  for each row execute function public.set_updated_at();

create trigger processos_set_updated_at
  before update on public.processos
  for each row execute function public.set_updated_at();

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, full_name, avatar_url)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name'),
    new.raw_user_meta_data ->> 'avatar_url'
  )
  on conflict (id) do update
    set full_name = excluded.full_name,
        avatar_url = excluded.avatar_url,
        updated_at = now();
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

